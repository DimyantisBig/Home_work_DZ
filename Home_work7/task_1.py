word = str(input("Введите ваше предложение : "))
word_s = word.split(" ") # Разбиваем строку на слова (по пробелам)
new_word = {}  # Создаем новый словарь
for w in word_s:
    if w in new_word:
        new_word[w] += 1  # Если слово есть, увеличиваем счетчик (то есть если слово встречается еще раз
                          # в словаре то в новом словаре увеличиваем счет на 1.
    else:
        new_word[w] = 1   # Если нет, добавляем его с количеством 1
print(new_word)
