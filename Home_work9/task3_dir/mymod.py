
                    # Функция count_lines принимает имя файла (name) и считает количество строк в файле
def count_lines(name):
                       # Открывает файл с указанным именем в режиме чтения
    with open(name) as text:
                       # Читаем все строки файла в список
        lines = text.readlines()
                       # Возвращаем длину списка строк, которая равна количеству строк в файле
        return len(lines)

