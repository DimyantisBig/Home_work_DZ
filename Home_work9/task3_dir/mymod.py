def count_chars(name):
                     # Открываем файл с указанным именем в режиме чтения
    with open(name) as text:
                     # Читаем весь текст файла как одну строку
        read = text.read()
                    # Возвращаем длину строки, которая равна количеству символов в файле
        return len(read)

